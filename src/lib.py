def decoders_by_coverage():
    names = dict()
    for coverage in sorted(set([d['coverage'] for d in decoders.values()])):
        num_decoders = 0
        print(coverage)
        names[coverage] = list()
        covered_decoders = dict()
        for k, v in decoders.items():
            if v['coverage'] == coverage:
                covered_decoders[k] = v
        for name, d in covered_decoders.items():
            num_decoders += 1
            names[coverage].append(name)
            print(f"  {num_decoders:02d}. {name:<30} {d['filename']:<30} {d['coverage']}")

        names[coverage] = sorted(names[coverage])
        print(f"    {len(names[coverage]):02d} decoders with {coverage} coverage")

    return names

